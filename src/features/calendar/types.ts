/**
 * Интерфейс для фильтров, применяемых к календарю проверок.
 */
export interface CalendarFilter {
  /**
   * Статус проверки. Может быть "all" для всех статусов, или конкретный статус.
   */
  status: "all" | "planned" | "pending" | "overdue" | "completed";
  /**
   * ID объекта, по которому фильтруются проверки. null, если выбраны все объекты.
   */
  objectId: number | null;
  /**
   * ID оператора, по которому фильтруются проверки. null, если выбраны все операторы.
   */
  operatorId: number | null;
}

/**
 * Интерфейс, представляющий собой одну проверку (событие в календаре).
 */
export interface Check {
  /**
   * Уникальный идентификатор проверки.
   */
  id: number;
  /**
   * Наименование объекта, на котором проводится проверка.
   */
  objectName: string;
  /**
   * Время начала проверки в формате ISO 8601 строки.
   * Пример: "2025-04-16T12:00:00"
   */
  startTime: string;
  /**
   * Текущий статус проверки.
   * - "planned": Запланировано (серый)
   * - "pending": Ожидается загрузка отчета (желтый)
   * - "overdue": Просрочено (красный)
   * - "completed": Проверено (зеленый)
   */
  status: "planned" | "pending" | "overdue" | "completed";
  /**
   * Информация об операторе, проводящем проверку.
   */
  operator: {
    id: number;
    fullName: string;
    avatar: string; // URL аватарки оператора
  };
  /**
   * ID объекта.
   */
  objectId: number;
  /**
   * Домен, к которому относится проверка (например, название филиала).
   */
  domain: string;
}

/**
 * Интерфейс, представляющий объект для выбора в фильтрах.
 */
export interface Object {
  /**
   * Уникальный идентификатор объекта.
   */
  id: number;
  /**
   * Наименование объекта.
   */
  name: string;
  /**
   * Домен, к которому относится объект.
   */
  domain: string;
}

/**
 * Интерфейс, представляющий оператора для выбора в фильтрах.
 */
export interface Operator {
  /**
   * Уникальный идентификатор оператора.
   */
  id: number;
  /**
   * Полное имя оператора.
   */
  fullName: string;
  /**
   * URL аватарки оператора.
   */
  avatar: string;
  /**
   * Домен, к которому относится оператор.
   */
  domain: string;
}